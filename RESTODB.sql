CREATE PROCEDURE SP_OBTENER_HORA_INICIO
@DATE_TIME DATETIME
AS
BEGIN 
SELECT HORA_INI FROM HORA_INICIO WHERE YEAR(HORA_INI)=YEAR(@DATE_TIME) AND ESTADO=1;
END;

GO
CREATE PROCEDURE SP_MODIFICAR_ESTADO_HORA
@DATE_TIME DATETIME 
AS
BEGIN 
    UPDATE HORA_INICIO SET ESTADO = 0 WHERE HORA_INI = @DATE_TIME;
    SELECT @@ROWCOUNT AS 'FilasModificadas';
END;